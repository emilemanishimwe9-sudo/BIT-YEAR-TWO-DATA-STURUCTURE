# Challenge: Stack operations
stack = []

# Step 1: Push elements
stack.append("1")
stack.append("2")
stack.append("3")

# Step 2: Pop two elements
stack.pop()
stack.pop()

# Step 3: Push "4"
stack.append("4")

# Step 4: Show top
print("Final stack:", stack)
print("Top of stack:", stack[-1])
